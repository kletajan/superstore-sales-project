    to_char(o.order_date, 'YYYY/MM') AS converted_date,
    SUM(f.sales) AS total_sales
FROM fact_sales f
JOIN dim_orders o ON f.order_id = o.order_id
GROUP BY converted_date
ORDER BY converted_date;
